Iterator* DBImpl::NewInternalIterator(const ReadOptions& options,
                                      SequenceNumber* latest_snapshot,
                                      uint32_t* seed) {
  mutex_.Lock();
  *latest_snapshot = versions_->LastSequence();

  // Collect together all needed child iterators
  std::vector<Iterator*> list;

  // memtable iter
  list.push_back(mem_->NewIterator());
  mem_->Ref();

  // immemtable iter
  if (imm_ != nullptr) {
    list.push_back(imm_->NewIterator());
    imm_->Ref();
  }

  // 当前version中的各个level中的sst
  // two level iter
  // level - 0 sst iter  (TableCache::NewIterator(),作为单个sstable iterator 的 TwoLevelIterator)
  // level - n sst iter 的集合  (（VersionSet::NewConcatenatingIterator(), 作为 sstable 集合iterator 的 TwoLevelIterator）
  versions_->current()->AddIterators(options, &list);

  // two level iter
  // 把获得的所有 Iterator 作为 children iterator 构造出 MergingIterator
  Iterator* internal_iter =
      NewMergingIterator(&internal_comparator_, &list[0], list.size());
  versions_->current()->Ref();

  IterState* cleanup = new IterState(&mutex_, mem_, imm_, versions_->current());
  internal_iter->RegisterCleanup(CleanupIteratorState, cleanup, nullptr);

  *seed = ++seed_;
  mutex_.Unlock();
  return internal_iter;
}

